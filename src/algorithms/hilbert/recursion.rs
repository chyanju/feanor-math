// ----------------------------------------------------------------------------
// Total split: every mixed term is coprime with every other â†’ factorise.
// ----------------------------------------------------------------------------

/// CoCoA `TotalSplitPoincare`: invoked when `BigPivotOf` returns no
/// most-frequent indet, i.e. every var appears at most once in the mixed
/// list.  Each mixed term is then independent and the numerator factorises
/// across them; SP contributes its own `(1 - t^{sp[i]})` factors.
fn total_split_poincare(zt: &ZT, mut tl: TermList) -> El<ZT> {
    let n_vars = tl.n_vars();
    let mixed = std::mem::take(&mut tl.mixed);
    // Swap SP out (EMonom isn't Default; use an empty placeholder).
    let mut sp = std::mem::replace(&mut tl.sp, EMonom::one(n_vars));
    let _ = tl;

    let mut res: Option<El<ZT>> = None;
    let mut coprime_degs: Vec<usize> = Vec::new();

    for term in mixed.iter() {
        let mut local_sp = EMonom::one(n_vars);
        super::term_list::move_not_coprime_sp(&mut sp, &mut local_sp, term);
        if local_sp.occ_count() != 0 {
            let factor = one_term_and_sp_poincare(zt, term, &local_sp);
            res = Some(match res {
                Some(prev) => zt.mul_ref(&prev, &factor),
                None => factor,
            });
        } else {
            coprime_degs.push(term.degree() as usize);
        }
    }

    let leftover = sp_poincare(zt, &sp);
    let mut res = match res {
        Some(prev) => zt.mul_ref(&prev, &leftover),
        None => leftover,
    };
    for d in coprime_degs {
        mul_by_1_minus_xk(zt, &mut res, d);
    }
    res
}

// ----------------------------------------------------------------------------
// SplitPoincare: variable-disjoint connected components.
// ----------------------------------------------------------------------------

/// CoCoA `SplitPoincare`: invoked when `SplitIndets` reveals â‰¥ 2 connected
/// components in the variable-incidence hypergraph.  Each component is
/// recursed on separately; SP and "isolated" mixed terms (those that fell
/// into a singleton group) get multiplied in at the end as plain
/// `(1 - t^k)` factors.
fn split_poincare(zt: &ZT, mut tl: TermList, splitter: Vec<EMonom>) -> El<ZT> {
    let n_vars = tl.n_vars();
    let mut isolated_degs: Vec<usize> = Vec::new();
    let mut res: Option<El<ZT>> = None;

    // For each splitter group, peel off a sub-TermList of `tl` consisting
    // of mixed/SP entries that intersect the group's support.  Recurse.
    for group in splitter.into_iter() {
        let mut sub = TermList::empty(n_vars);
        move_not_coprime(&mut tl, &mut sub, &group);
        // CoCoA's optimisation: if the sub has exactly one mixed term and
        // empty SP, the recursion would just yield `(1 - t^{deg m})`, so
        // we queue the degree and skip the call.
        if sub.mixed.len() == 1 && sub.sp.occ_count() == 0 {
            isolated_degs.push(sub.mixed[0].degree() as usize);
        } else {
            let sub_h = big_rec_poincare(zt, sub);
            res = Some(match res {
                Some(prev) => zt.mul_ref(&prev, &sub_h),
                None => sub_h,
            });
        }
    }

    // Leftover: SP entries never absorbed by any group, plus any mixed
    // terms that survived (none should, since SplitIndets covered every
    // mixed term, but be defensive).
    debug_assert!(tl.mixed.is_empty(), "SplitPoincare: mixed not fully consumed");
    let leftover = sp_poincare(zt, &tl.sp);
    let mut res = match res {
        Some(prev) => zt.mul_ref(&prev, &leftover),
        None => leftover,
    };
    for d in isolated_degs {
        mul_by_1_minus_xk(zt, &mut res, d);
    }
    res
}

// ----------------------------------------------------------------------------
// BigRecPoincare: the main recursion driver.
// ----------------------------------------------------------------------------

/// CoCoA `BigRecPoincare`.  Recursively computes the Hilbert numerator of
/// `R / I` where `I` is the monomial ideal generated by `tl.sp âˆª tl.mixed`.
pub fn big_rec_poincare(zt: &ZT, mut tl: TermList) -> El<ZT> {
    if tl.mixed.is_empty() {
        return sp_poincare(zt, &tl.sp);
    }
    if tl.mixed.len() == 1 {
        return len_one_poincare(zt, &tl);
    }
    let pivot = match big_pivot_of(&tl) {
        None => {
            // No var occurs more than once â†’ total split.
            return total_split_poincare(zt, tl);
        }
        Some((idx, deg)) => (idx, deg),
    };
    let (pi, pe) = pivot;

    // Try a connected-components split iff the recursion is in the
    // "interesting" regime (CoCoA: few mixed terms, many vars).
    if tl.mixed.len() < tl.n_vars() && tl.mixed.len() > 4 {
        if let Some(splitter) = split_indets(&tl) {
            return split_poincare(zt, tl, splitter);
        }
    }

    // Standard pivot recursion: H = H(I + x_pi^pe) + t^pe * H(I : x_pi^pe).
    let div = reduce_and_divide_by_simple_power(&mut tl, pi, pe);
    let mut h_kept = big_rec_poincare(zt, tl);
    let h_div = big_rec_poincare(zt, div);
    // h_kept += t^pe * h_div.
    let one_b = zt.base_ring().one();
    let h_div_terms: Vec<_> = zt
        .terms(&h_div)
        .map(|(c, d)| (zt.base_ring().mul_ref(&one_b, c), d + pe as usize))
        .collect();
    zt.get_ring().add_assign_from_terms(&mut h_kept, h_div_terms);
    h_kept
}

